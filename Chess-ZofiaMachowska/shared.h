/**
 * @brief Checks if given coordinates (x, y) are within the bounds of a chessboard.
 *
 * This function validates whether the provided x and y coordinates lie within
 * the 8x8 chessboard grid.
 *
 * @param x The x-coordinate to be checked.
 * @param y The y-coordinate to be checked.
 * @return true if the coordinates are on the board, false otherwise.
 */
bool isOnBoard(int x, int y);


